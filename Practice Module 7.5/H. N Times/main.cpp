#include <bits/stdc++.h>

using namespace std;

int main()
{

    int test;
    cin>>test;

    while(test>=1)
    {

        int n;
        cin>>n;
        char c;
        cin>>c;

        for(int i=0;i<n;i++){
            cout<<c<<" ";
        }
        cout<<endl;


        test--;
    }

    return 0;
}
